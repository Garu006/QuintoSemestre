public class Calculadora {
    // crear las 4 funciones de una calculadora
    public Calculadora(){}

    public int sumar(int a, int b){
        return a+b;
    }
    public int restar(int a, int b){
        return a-b;
    }
    public int multiplicar(int a, int b){
        return a*b;
    }
    public int dividr(int a, int b){
        return a/b;
    }
}
